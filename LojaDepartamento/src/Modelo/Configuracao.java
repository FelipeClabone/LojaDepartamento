package Modelo;

/**
 *
 * @author Felipe de Freitas
 */
public class Configuracao {
    private String arquivoVendas;
    private String arquivoProdutos;
    private String arquivoFornecedores;
    private String arquivoUsuarios;

    public String getArquivoVendas() {
        return arquivoVendas;
    }

    public void setArquivoVendas(String arquivoVendas) {
        this.arquivoVendas = arquivoVendas;
    }

    public String getArquivoProdutos() {
        return arquivoProdutos;
    }

    public void setArquivoProdutos(String arquivoProdutos) {
        this.arquivoProdutos = arquivoProdutos;
    }

    public String getArquivoFornecedores() {
        return arquivoFornecedores;
    }

    public void setArquivoFornecedores(String arquivoFornecedores) {
        this.arquivoFornecedores = arquivoFornecedores;
    }

    public String getArquivoUsuarios() {
        return arquivoUsuarios;
    }

    public void setArquivoUsuarios(String arquivoUsuarios) {
        this.arquivoUsuarios = arquivoUsuarios;
    }
}
